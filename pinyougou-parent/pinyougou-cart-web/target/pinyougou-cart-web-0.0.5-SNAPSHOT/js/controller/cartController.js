app.controller("cartController",function($scope,cartService){
    //购物车列表
    $scope.findCartList=function(){
        cartService.findCartList().success(
            function(response){
                $scope.cartList=response;
                $scope.totalValue=cartService.sum($scope.cartList);//求合计数
            }
        );
    }
    //商品增减
    $scope.addGoodsToCartList=function(itemId,num){
        cartService.addGoodsToCartList(itemId,num).success(
            function(response){
                if(response.success){
                    $scope.findCartList();//刷新列表
                }else{
                    alert(response.message);
                }
            }
        );
    }
    //获取当前用户地址列表
    $scope.findAddressList=function(){
        cartService.findAddressList().success(
            function(response){
                $scope.addressList=response;
            }
        );
    }
    //删除当前用户地址列表
    $scope.deleteAddress=function(){
        cartService.deleteAddress().success(
            function(response){
                if(response.success){
                    alert(response.message);
                }else{
                    alert(response.message);
                }
            }
        );
    }
    //点击选择当前地址
    $scope.selectAddress=function(address){
        $scope.address=address;
    }
    //判断当前地址是否选中
    $scope.isSelectAddress=function(address){
        if(address==$scope.address){
            return true;
        }else{
            return false;
        }
    }
    $scope.order={paymentType:'1'};//订单对象

    $scope.selectPayType=function(type){
        $scope.order.paymentType=type;
    }

    $scope.submitOrder=function(){
        //将页面选择得收获地址信息绑定到订单信息order中,提交到后台
        $scope.order.receiverAreaName=$scope.address.address;//前端页面选择地址值
        $scope.order.receiverMobile=$scope.address.mobile;//前端页面选择电话
        $scope.order.receiver=$scope.address.contact;//前端页面选择联系人

        cartService.submit($scope.order).success(
            function(response){
                if(response.success){
                    if($scope.order.paymentType=='1'){//如果是微信支付,跳转支付页面
                        location.href='pay.html';
                    }else{//如果是货到付款,跳转到提示页面
                        location.href='paysuccess.html';
                    }
                }else{
                   alert(response.message);
                }
            }
        );
    }

});